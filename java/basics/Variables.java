public class Variables {
    public static void main(String[] args) {
        // 変数の宣言と初期化
        int age = 25;
        double height = 175.5;
        String name = "Alice";

        // 出力
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " cm");
    }
}
